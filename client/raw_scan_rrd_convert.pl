#!/usr/bin/env perl
use List::MoreUtils 'any';

my $RRD_DIR = '/home/kb8u/tvdx/rrd';
my @rrd_files = glob("$RRD_DIR/$ARGV[0]_$ARGV[1]_*.rrd");
if (! @rrd_files) {
  print "No rrd files found for tuner ID $ARGV[0] tuner $ARGV[1]\n";
  exit;
}

my %php_calls;
while(<DATA>) {
  chomp;
  my @line = split /\s+/, $_;
  my $php_call = $line[1];
  next if $php_call =~ /^P/;
  $php_call =~ /([WKXC]\d{0,2}[A-Z]{2,})/;
  my $shortened_php_call = $1;
  if (any { $_ eq $shortened_php_call } (keys %php_calls)) {
#    print "duplicate for $shortened_php_call: $php_calls{$shortened_php_call} and $php_call\n";
    next;
  }
  $php_calls{$shortened_php_call} = $php_call;
}

foreach my $rrd (@rrd_files) {
  $rrd =~ /_([WKXC][A-Z]{2,}).rrd$/;
  my $rrd_call = $1;
  if (! exists $php_calls{$rrd_call}) {
    print "can't process $rrd\n";
    next;
  }
  $new_rrd = $rrd;
  $new_rrd =~ s/[WKXC][A-Z]{2,}/$php_calls{$rrd_call}/;
  if ($new_rrd eq $rrd) {
    print "skipping $rrd_call\n";
    next;
  }
  print "mv $rrd $new_rrd\n";
  `mv $rrd $new_rrd`;
  
}


