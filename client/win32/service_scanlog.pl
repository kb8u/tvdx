# scan tuner on Silicon Dust HDHomeRun to collect TV station channel, callsign
# and strength.  Optionally writes DX found to log file.
# Sends resluts to web site.
#
# Written November 25, 2010 by Russell Dwarshuis.
# Added TSID look ups April 22, 2012
# This version runs as a service.  Options for other tuners or etc. are not
# supported.
#

use strict 'vars';
use Win32;
use Cava::Pack;
use LWP;
use XML::Simple;
use LWP::Simple;

my $ua = LWP::UserAgent->new;

# prevent child processes from opening a console window
Win32::SetChildShowWindow(0);

my $SPOT_URL = 'http://kb8u.ham-radio-op.net/tvdx/automated_spot';
my $TSID_URL = 'http://www.rabbitears.info/rawtsid.php';
my $path = Cava::Pack::GetUserAppDataDir();
my $TSID_FILE = $path . '\rawtsid.php';
my $TSID_FILE_LONG = Cava::Pack::DisplayPath($TSID_FILE);

# program used to interface with tuner
my $CONFIG_PROGRAM = 'C:\Progra~1\Silicondust\HDHomeRun\hdhomerun_config.exe';

# tuner ID; can use FFFFFFFF if it's the only one on network
my $TUNER_ID = 'FFFFFFFF';

# which tuner to scan
my $TUNER = '/tuner0/';

my $DEBUG = 0;

if (! -x $CONFIG_PROGRAM) {
  print "$CONFIG_PROGRAM not found or can't be run.\n";
  exit;
}

my $found_tuner_id;
open DISCOVER, "\"$CONFIG_PROGRAM\" discover |" or die "Can run $CONFIG_PROGRAM discover";
while(<DISCOVER>) {
  if ($_ =~ /device\s+([0-9A-Fa-f]{8})\s+found/i) {
    $found_tuner_id = uc $1;
    print "Found hdhomerun device $found_tuner_id\n" if $DEBUG;
  }
}

my $user_id = "TunerID_$found_tuner_id" . "_$TUNER";
$user_id =~ s/\///g; # get rid of slashes in /tunerX/ for XML names to be proper


SCAN: while(1) {
  my @dx; # DX station information
  my %scan; # information reportd to web site
  my %TSID; # transport stream identifiers from $TSID_URL or local

  # get fresh TSID data if needed
  load_tsid(\%TSID);

  # scan tuner
  print "trying to run $CONFIG_PROGRAM $TUNER_ID scan $TUNER\n" if $DEBUG;
  open SCAN, "\"$CONFIG_PROGRAM\" $TUNER_ID scan $TUNER |" or die "can't run scan";
  my ($freq,$channel,$modulation,$strength,$sig_noise,$symbol_err,$tsid,$program,$number,$name,$callsign_seen);
  while(<SCAN>) {
    print $_ if $DEBUG;
    if ($_ =~ /^SCANNING:\s+(\d+)/) {
      # is a new scan starting but no callsign was seen and TSID was seen?
      if ($channel && (! $callsign_seen) && exists $TSID{$tsid}) {
	my ($t_call,$t_channel) = split /\s+/, $TSID{$tsid};
	# if channel matches also then use the TSID hash for call
	if ($t_channel == $channel) {
          $callsign_seen = $t_call;
          print "Found call $callsign_seen channel $channel TSID $tsid strength $strength sig_noise $sig_noise from TSID\n" if $DEBUG;
          $scan{'user_id'} = $user_id;
          push @{$scan{'tv_signal'}}, { 'callsign'        => $callsign_seen,
                                        'virtual_channel' => $number,
                                        'sig_noise'       => $sig_noise,
                                        'strength'        => $strength,
                                        'rf_channel'      => $channel, };
          push @dx, "RF-channel: $channel: strength: $strength sig/noise: $sig_noise call: $callsign_seen virtual-channel: $number\n";
	}
      }
      $freq = '';
      $channel = '';
      $modulation = '';
      $strength = '';
      $sig_noise = '';
      $symbol_err = '';
      $tsid = '';
      $program = '';
      $number = '';
      $name = '';
      $callsign_seen = 0;
    }
    if ($_ =~ /^SCANNING:\s+(\d+)\s+\(us-bcast:(\d+)/) {
      $freq = $1;
      $channel = $2;
    }
    if ($_ =~ /^LOCK:\s+(\S+)\s+\(ss=(\d+)\s+snq=(\d+)\s+seq=(\d+)\)/) {
      $modulation = $1;
      $strength   = $2;
      $sig_noise  = $3;
      $symbol_err = $4;
    }
    if ($_ =~ /^TSID: (0x[0-9A-Fa-f]{4})$/) {
      $tsid = hex($1);
    }
    if ($_ =~ /^PROGRAM\s+(\d+):\s+(\S+)\s+(\S+)/) {
      $program = $1;
      $number = $2;
      $name = $3;
    }
    if ($channel && (! $callsign_seen) && $name =~ /^([CWKX][A-Z]{2,3})/) {
      $callsign_seen = uc $1;
      print "Found call $callsign_seen channel $channel strength $strength sig_noise $sig_noise\n" if $DEBUG;
      $scan{'user_id'} = $user_id;
      push @{$scan{'tv_signal'}}, { 'callsign'        => $callsign_seen,
                                    'virtual_channel' => $number,
                                    'sig_noise'       => $sig_noise,
                                    'strength'        => $strength,
                                    'rf_channel'      => $channel, };
      push @dx, "RF-channel: $channel: strength: $strength sig/noise: $sig_noise call: $callsign_seen virtual-channel: $number\n";
    }
  }

  print "scan finished.\n" if $DEBUG;

  # Nothing more to do unless channels were found
  if (! scalar @{$scan{'tv_signal'}}) {
    print "No channels found in scan!  Waiting 10 seconds before trying again...\n" if $DEBUG;
    sleep 10; # don't try to rapidly run hdhomerun_config over and over on a locked tuner
    next SCAN;
  }

  my $xml = XMLout(\%scan);

  print "Sending results to $SPOT_URL\n" if $DEBUG;
  my $req = HTTP::Request->new(POST => $SPOT_URL);
  $req->content_type('application/x-www-form-urlencoded');
  $req->content("xml=$xml");
  my $res = $ua->request($req);
  
  if ($DEBUG) {
    print "Checking if web page got results ok\n";
    if ($res->is_success) {
      print $res->content;
      print "\n";
    }
    else {
      print $res->status_line, "\n";
    }
  }

}


sub load_tsid {
  my ($tsid_ref) = @_;

  # if TSID file is older than 1 week or is non-existent, download a new
  # file and read from it
  if (! -r $TSID_FILE || -M $TSID_FILE > 7) {
    my $rc = getstore($TSID_URL,$TSID_FILE);
    if (is_error($rc) && $DEBUG) {
      print "Couldn't download $TSID_URL or save it to $TSID_FILE.  Got $rc\n";
    }
  }
  
  open TSID, $TSID_FILE;
  if (fileno(TSID)) {
    while(<TSID>) {
      my ($tsid,$call,$ch1,$ch2,$state,$city) = split /\s+/,$_;
      # change WXPD-TV or similar to just WXPD
      ($call) = split /\-/,$call;
        $tsid_ref->{$tsid} = "$call $ch2";
    }
  }
  close TSID;

  # if there isn't a lot of entries, there must be some error.  Use
  # hard-coded values if all else fails
  if (scalar keys %{$tsid_ref} < 2000) {
     print "$TSID_FILE couldn't be read or has too few entries.  Using defaults\n" if $DEBUG;
     load_default_tsid($tsid_ref);
  }
}


